def addition(number_first: int, number_second: int):
    return number_first+number_second

def subtraction(number_first: int, number_second: int):
    return number_first-number_second

def multiplication(number_first: int, number_second: int):
    return number_first*number_second

def arithmetic_operators(number_first: int, number_second: int):
    addition(number_first,number_second)
    subtraction(number_first,number_second)
    multiplication(number_first,number_second)